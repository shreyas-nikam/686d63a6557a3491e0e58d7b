
import streamlit as st
def run_page2():
    st.title("Page 2: Understanding Income Statement Structure")
    st.markdown("""
    This page provides a general overview of the standard structure and key components of an income statement.
    *   **Revenue:** Top line, total sales.
    *   **Cost of Goods Sold (COGS):** Direct costs attributable to the production of goods.
    *   **Gross Profit:** Revenue - COGS.
    *   **Operating Expenses:** Sales, General & Administrative (SG&A), Research & Development (R&D).
    *   **Operating Income (EBIT):** Gross Profit - Operating Expenses.
    *   **Interest Expense:** Cost of borrowing.
    *   **Income Before Taxes (EBT):** Operating Income - Interest Expense.
    *   **Income Tax Expense:** Taxes on earnings.
    *   **Net Income:** The 'bottom line' profit.
    """)
