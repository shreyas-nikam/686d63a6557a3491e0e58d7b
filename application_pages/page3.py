
import streamlit as st
def run_page3():
    st.title("Page 3: Key Income Statement Ratios")
    st.markdown("""
    This page explains important ratios derived from the income statement, which help in financial analysis.

    **1. Gross Profit Margin:** Measures profitability after deducting production costs.
    $$ \text{Gross Profit Margin} = \frac{\text{Gross Profit}}{\text{Revenue}} $$

    **2. Operating Profit Margin:** Indicates efficiency in managing operations.
    $$ \text{Operating Profit Margin} = \frac{\text{Operating Income}}{\text{Revenue}} $$

    **3. Net Profit Margin:** Shows how much net income is generated per dollar of revenue.
    $$ \text{Net Profit Margin} = \frac{\text{Net Income}}{\text{Revenue}} $$
    """)
